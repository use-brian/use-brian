/**
 * Unit tests for the V2 skill-review worker (WS-B).
 *
 * The worker has three failure-isolation surfaces:
 *   * Per-workspace rate cap — skip cycle + log + still mark reviewed
 *   * Per-skill review lease — first-writer-wins
 *   * Per-action failure — isolated; cycle continues
 *
 * These tests target the per-session `reviewSession` entry point with
 * faked stores so the cycle can be exercised in-memory. The
 * `countTodayOps` helper hits the real `query` import, so the rate-cap
 * branch is exercised via `dailyOpCap: 0` (which forces the cap check to
 * trip even when there are no analytics events).
 *
 * Spec: `docs/architecture/engine/skill-system.md` → "Failure modes" +
 * "Per-workspace rate cap" + "Concurrent-review lease".
 */

