/**
 * Recursively widens string literal types to plain `string` so other
 * locale dictionaries can match the shape without being constrained to
 * the exact English text. Mirrors the pattern used in apps/web.
 */
type WidenStrings<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly WidenStrings<U>[]
    : T extends object
      ? { [K in keyof T]: WidenStrings<T[K]> }
      : T;

export type Dictionary = WidenStrings<typeof en>;
